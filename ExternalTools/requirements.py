# requirements.txt file is a plain text file that lists all the Python packages (dependencies) your project needs. It allows anyone (or any deployment server) to install the exact libraries required for your application.

# It allows anyone working on the project to install the exact dependencies with a single command.

# command is - pip install -r requirements.txt

